package advanced;
//類別：理論上沒有實體，去訂定結構
//物件：架構與行為不會改變

public class Car {			//類別
	public String brand;	//屬性
	public String color;	//屬性
	
	public void go() {		//method方法
		System.out.println("出發");
	}
}
//Java的核心價值為"物件導向"，四個字。
//物件：–––––––––→【屬性與方法】集合體–––––––––→類別–––––––––→物件的藍圖
//	 導向                             定義：屬性、方法
//   /	\
// 以"物件"為主
