package helloworld;

public class BitwiseOperators { // 位元運算(Bitwise Operations)
	public static void main(String[] args) {

		// 老師舉例：
		// b1 = 12 (8+4)
		// b2 = 10 (8+2)
		byte b1 = 0b1100;
		byte b2 = 0b1010;

		// 1. AND (&) 及：兩者皆為 1 才是 1 (全真才真)
		// -------------------------
		// b1 位元： 1 1 0 0
		// 面額表 ： 8 | 4 | 2 | 1
		// b2 位元： 1 0 1 0
		// -------------------------
		// 結果位元： 1 0 0 0 => 只剩下 8 元亮燈，印出 8
		System.out.println("AND (&) 結果：" + (b1 & b2));
		System.out.println(Integer.toBinaryString(b1 & b2));

		// 2. OR (|) 或：只要有一個 1 就是 1 (有真即真)
		// -------------------------
		// b1 位元： 1 1 0 0
		// 面額表 ： 8 | 4 | 2 | 1
		// b2 位元： 1 0 1 0
		// -------------------------
		// 結果位元： 1 1 1 0 => 8+4+2 亮燈，印出 14
		System.out.println("OR (|) 結果：" + (b1 | b2));
		System.out.println(Integer.toBinaryString(b1 | b2));

		// 3. XOR (^) 互斥或：兩者不同才是 1 (有夠反骨)
		// -------------------------
		// b1 位元： 1 1 0 0
		// 面額表 ： 8 | 4 | 2 | 1
		// b2 位元： 1 0 1 0
		// -------------------------
		// 結果位元： 0 1 1 0 => 只有 4+2 亮燈，印出 6
		System.out.println("XOR (^) 結果：" + (b1 ^ b2));
		// 💡：因為最左邊是 0，電腦預設會省略，所以手動補一個 "0" 讓它對齊面額表
		System.out.println("0" + Integer.toBinaryString(b1 ^ b2));

	
		// 4. NOT (~) 非：【0 變 1，1 變 0】 -> 這就是「1 補數」
		// ⚠️ 重要觀念：Java 會將 byte 提升為 int (32位元)，前面看不見的 0 全都會變 1
		// ---------------------------------------------------------
		Object o = ~b1;
		System.out.println("運算後的資料型態：" + o.getClass().getSimpleName()); // Integer

		// --- 補數邏輯拆解 (以 b1 = 12 為例) ---
		// 1. 原碼 (12) ： 0000...0000 1100 (前面有 28 個 0)
		// 2. 1 補數 (~)： 1111...1111 0011 (執行 NOT，全反轉)
		// 3. 2 補數 (+1)： 1111...1111 0100 (這是電腦認定的 -12)
		// ---------------------------------------------------------
		// 💡 為什麼 ~12 印出 -13？
		// 因為 1 補數結果 (11...0011) 比 2 補數結果 (11...0100) 少了 1
		// 所以公式就是：~x = -(x + 1) => -(12 + 1) = -13

		// 反轉前 (1100) ： 1 1 0 0
		// 面額表 (最後四位)： 8 | 4 | 2 | 1
		// 反轉後 (0011) ： 0 0 1 1 (執行 ~b1 後的結果)

		System.out.println("NOT (~) 二進制驗算：" + Integer.toBinaryString(~b1));
		System.out.println("NOT (~) 十進制結果：" + (~b1)); // 印出 -13
	}
}
