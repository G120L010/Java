package helloworld;

public class Conversion {
	public static void main(String[] args) {
		/* --- 重點一：小數字練習 (以 37 為例) ---
		 * 目的：建立「分組對照」與「基本位值」觀念 */
		
		// 1. 二進制 (Binary)：開頭 0b
		// 技巧：由右往左「四位一組」(0010 0101)，每四位剛好對應一位十六進制。
		// 二進制位數：  1   0   0   1   0   1  (從右往左看：1, 2, 4, 8, 16, 32)
		// 計算過程  ：(1*32) + (1*4) + (1*1) = 37
		int i = 0b100101; 
		
		// 2. 十六進制 (Hexadecimal)：開頭 0x
		// 技巧：與二進制「四位一組」對應。0x25 的 2 是 0010，5 是 0101。
		// 十六進位數：  2       5  (從右往左看：1, 16, 256)
		// 計算過程  ：(2*16) + (5*1) = 32 + 5 = 37
		int i2 = 0x25; 
		
		// 3. 八進制 (Octal)：開頭 0 (數字零)
		// 技巧：與二進制「三位一組」(000 100 101) 對應。
		// 八進位數  ：  4       5  (從右往左看：1, 8, 64)
		// 計算過程  ：(4*8) + (5*1) = 32 + 5 = 37
		int i3 = 045; 

		System.out.println("二進制 (0b100101) 印出：" + i);  
		System.out.println("十六進制 (0x25)    印出：" + i2); 
		System.out.println("八進制 (045)       印出：" + i3); 
		System.out.println();

		/* --- 重點二：大數字進階練習 (以 1000 為例) ---
		 * 目的：運用【大數扣除法】垂直填表邏輯
		 * 邏輯：夠減填 1 (紀錄餘額)，不夠減填 0 (餘額不變)。*/
		
		// 餘額： 1000 |  488 |  232 |  104 |   40 |    8 |    8 |    0 |   0 |  0 
		// 面額：  512 |  256 |  128 |   64 |   32 |   16 |    8 |    4 |   2 |  1
		// 填入：   1  |   1  |   1  |    1 |    1 |    0 |    1 |    0 |   0 |  0
		// ----------------------------------------------------------------------
		// 組合結果：0b1111101000
		int Binary = 0b1111101000; 

		// 十六進制對照 (四位一組)：  3(0011)   E(1110)   8(1000)
		int Hex = 0x3E8;

		// 八進制對照 (三位一組)： 1(001)  7(111)  5(101)  0(000)
		int Oct = 01750;

		//測試 1000 的大數扣除法結果
		System.out.println("二進制扣除法結果印出：" + Binary);
		System.out.println("十六進制 (0x3E8) 印出：" + Hex);
		System.out.println("八進制 (01750)   印出：" + Oct);
	}
}
