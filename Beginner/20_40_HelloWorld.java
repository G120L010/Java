package helloworld;
/* 在專案中的類別HelloWorld建立人生首支程式
 * 功能：在主控台輸出 "Hello World!!" */
public class HelloWorld {

	/**
	 * 主程式進入點 
	 * @param args 命令列參數 */
	public static void main(String[] args) {

		// 在主控台(Console)輸出文字
		System.out.println("Hello World!!");

		// ===== Eclipse 常用快捷鍵 =====

		// 快速註解 / 取消註解
		// Ctrl + /

		// 自動提示 / 自動完成 (Content Assist)
		// Alt + /

		// 常用自動完成範例：
		// main + Alt + / → 產生 main 方法
		// sysout + Alt + / → 產生 System.out.println()

		// 程式碼行移動（上下搬移）
		// Alt + ↑ / ↓

		// 複製程式碼行（向上或向下複製）
		// Ctrl + Alt + ↑ / ↓

		// 自動格式化程式碼（排版）
		// Ctrl + Shift + F
	}
}
